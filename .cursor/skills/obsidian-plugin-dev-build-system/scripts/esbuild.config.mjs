    const result = await esbuild.build(buildOptions);
    
    // 检查构建结果
    if (result.errors.length > 0) {
        console.error(`\n[ERROR] [${pluginName}] 构建失败！`);
        console.error(`   错误数量: ${result.errors.length}`);
        result.errors.forEach((error, index) => {
            console.error(`\n   错误 ${index + 1}:`);
            console.error(`   ${error.text}`);
            if (error.location) {
                console.error(`   位置: ${error.location.file}:${error.location.line}:${error.location.column}`);
            }
        });
        process.exit(1);
    }
    
    if (result.warnings.length > 0) {
        console.warn(`\n[WARNING] [${pluginName}] 构建警告:`);
        result.warnings.forEach((warning, index) => {
            console.warn(`   警告 ${index + 1}: ${warning.text}`);
        });
    }
    
    // 构建成功后，插件回调会自动执行复制操作
    console.log(`\n[OK] [${pluginName}] TypeScript 编译完成`);
    
    // 验证构建产物
    const mainJsPath = path.join(outDir, "main.js");
    if (fs.existsSync(mainJsPath)) {
        const stats = fs.statSync(mainJsPath);
        console.log(`   [OK] 构建产物已生成: ${mainJsPath}`);
        console.log(`   [OK] 文件大小: ${(stats.size / 1024).toFixed(2)} KB`);
    } else {
        console.error(`   [ERROR] 构建产物不存在: ${mainJsPath}`);
        process.exit(1);
    }
    
    console.log("\n" + "=".repeat(60));
    console.log(`[OK] [${pluginName}] 构建完成！`);
    console.log(`   版本: ${manifest.version}`);
    console.log(`   输出目录: ${outDir}`);
    console.log("=".repeat(60) + "\n");
    process.exit(0);
