// 递归复制目录的函数
/**
 * @param {string} src - 源目录路径
 * @param {string} dest - 目标目录路径
 * @param {{ count: number }} copiedCount - 已复制的文件数量（内部使用）
 * @returns {number} - 复制的文件总数
 */
const copyDirectory = (src, dest, copiedCount = { count: 0 }) => {
    // 创建目标目录
    if (!fs.existsSync(dest)) {
        fs.mkdirSync(dest, { recursive: true });
    }

    // 读取源目录中的所有文件和子目录
    const entries = fs.readdirSync(src, { withFileTypes: true });

    for (const entry of entries) {
        const srcPath = path.join(src, entry.name);
        const destPath = path.join(dest, entry.name);

        if (entry.isDirectory()) {
            // 递归复制子目录
            copyDirectory(srcPath, destPath, copiedCount);
        } else {
            // 复制文件（直接覆盖）
            fs.copyFileSync(srcPath, destPath);
            copiedCount.count++;
            console.log(`     [OK] ${entry.name}`);
        }
    }

    return copiedCount.count;
};

// 在父级目录中查找 .obsidian/plugins 目录
/**
 * @param {string} startDir - 开始查找的目录
 * @returns {string | null} - 找到的 plugins 目录路径，未找到则返回 null
 */
const findObsidianPluginsDir = startDir => {
    let currentDir = path.resolve(startDir);
    const root = path.parse(currentDir).root;

    console.log(`[SEARCH] [${pluginName}] 正在查找 Obsidian 插件目录...`);

    // 向上遍历目录树
    while (currentDir !== root) {
        // 检查当前目录的父目录
        const parentDir = path.dirname(currentDir);
        const obsidianDir = path.join(parentDir, ".obsidian");

        if (fs.existsSync(obsidianDir) && fs.statSync(obsidianDir).isDirectory()) {
            const pluginsDir = path.join(obsidianDir, "plugins");
            if (fs.existsSync(pluginsDir) && fs.statSync(pluginsDir).isDirectory()) {
                console.log(`   [OK] 找到插件目录: ${pluginsDir}`);
                return pluginsDir;
            }
        }

        // 移动到父目录
        currentDir = parentDir;
    }

    console.log("   [ERROR] 未找到 Obsidian 插件目录");
    return null;
};

// 复制构建产物到上级目录的 .obsidian/plugins/ 目录
const copyToParentRepo = () => {
    console.log("\n" + "=".repeat(50));
    console.log(`[PACKAGE] [${pluginName}] 复制构建产物到上级目录`);
    console.log("=".repeat(50));

    if (!COPY_TO_PARENT_REPO) {
        console.log("[WARNING] 复制到上级目录功能已禁用");
        console.log("=".repeat(50) + "\n");
        return;
    }

    try {
        // 查找上级目录的 .obsidian/plugins 目录
        const pluginsDir = findObsidianPluginsDir(PROJECT_ROOT);

        if (!pluginsDir) {
            console.log(`\n[WARNING] [${pluginName}] 跳过复制：未找到上级目录的 Obsidian 插件目录`);
            console.log("   提示：此功能需要项目位于 Obsidian 仓库的子目录中");
            console.log("=".repeat(50) + "\n");
            return;
        }

        // 目标目录：上级目录的 .obsidian/plugins/{pluginName}
        const targetDir = path.join(pluginsDir, pluginName);

        // 检查源目录是否存在
        if (!fs.existsSync(outDir)) {
            console.log(`\n[WARNING] [${pluginName}] 跳过复制：源目录不存在`);
            console.log(`   源目录: ${outDir}`);
            console.log("=".repeat(50) + "\n");
            return;
        }

        console.log(`\n[FOLDER] [${pluginName}] 开始复制文件...`);
        console.log(`   源目录: ${outDir}`);
        console.log(`   目标目录: ${targetDir}`);

        // 检查目标目录是否存在
        const isUpdate = fs.existsSync(targetDir);
        if (isUpdate) {
            console.log("   [INFO] 目标目录已存在，将更新现有文件");
        } else {
            console.log("   [INFO] 目标目录不存在，将创建新目录");
        }

        console.log("\n   正在复制文件:");
        // 复制整个插件目录
        const fileCount = copyDirectory(outDir, targetDir);

        console.log(`\n   [OK] 插件 [${pluginName}] 复制成功！`);
        console.log(`   文件数量: ${String(fileCount)} 个`);
        console.log(`   版本: v${manifest.version}`);
        if (isUpdate) {
            console.log(`   状态: 已更新到最新版本`);
        } else {
            console.log(`   状态: 已复制到上级目录`);
        }

        // 处理 .hotreload 文件
        if (ENABLE_HOTRELOAD) {
            try {
                const hotreloadPath = path.join(targetDir, ".hotreload");
                
                // 如果构建目录中有 .hotreload 文件，则复制它
                const sourceHotreloadPath = path.join(outDir, ".hotreload");
                if (fs.existsSync(sourceHotreloadPath)) {
                    fs.copyFileSync(sourceHotreloadPath, hotreloadPath);
                    console.log("\n   [HOT] 热重载文件已复制");
                } else {
                    // 否则创建新的 .hotreload 文件
                    fs.writeFileSync(hotreloadPath, "");
                    console.log("\n   [HOT] 热重载文件已创建");
                }
                console.log("   提示：需要安装 Hot-Reload 插件才能使用此功能");
            } catch (error) {
                console.log("\n   [WARNING] 热重载文件处理失败:");
                console.log(`      错误: ${error instanceof Error ? error.message : error}`);
            }
        } else {
            // 如果选项被关闭，清理目标目录的 .hotreload 文件
            const hotreloadPath = path.join(targetDir, ".hotreload");
            if (fs.existsSync(hotreloadPath)) {
                try {
                    fs.unlinkSync(hotreloadPath);
                    console.log("\n   [DELETE] 热重载文件已删除（功能已禁用）");
                } catch (error) {
                    console.log("\n   [WARNING] 删除热重载文件失败:");
                    console.log(`      错误: ${error instanceof Error ? error.message : error}`);
                }
            } else {
                console.log("\n   [INFO] 未找到热重载文件，无需清理");
            }
        }
        if (ENABLE_HOTRELOAD) {
            console.log("\n   [TIP] 提示：Hot-Reload 插件将自动重载更新");
        } else {
            console.log("\n   [TIP] 提示：重启 Obsidian 或禁用后重新启用插件以加载最新版本");
        }
        console.log("=".repeat(50) + "\n");
    } catch (error) {
        console.log(`\n   [ERROR] [${pluginName}] 复制失败！`);
        console.log(`      错误: ${error instanceof Error ? error.message : error}`);
        console.log("=".repeat(50) + "\n");
        // 不抛出错误，避免中断构建过程
    }
};

